// 5. Imprimir números pares de 0 a 20 utilizando o loop while

let numero = 0; // inicializacão

// Condicão
while (numero <= 20) {
    //Só entra no corpo se a condição for verdadeira
    console.log(numero);
    //numero = numero + 2;
    numero += 2; //soma de 2 em 2, ou seja, somente numeros pares
}